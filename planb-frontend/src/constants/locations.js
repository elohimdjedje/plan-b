// Liste des villes pour les selecteurs (triee)
export const CITIES_LIST = Object.keys(IVORY_COAST_LOCATIONS).sort();

// Fonction pour obtenir les communes d'une ville
export const getCommunes = (city) => {
  if (!city || !IVORY_COAST_LOCATIONS[city]) {
    return [];
  }
  return IVORY_COAST_LOCATIONS[city].communes;
};

// Fonction pour verifier si une commune existe pour une ville
export const isValidCommune = (city, commune) => {
  const communes = getCommunes(city);
  return communes.includes(commune);
};

